package Lab2;

public class Dog {

    String color;
    float weight;

    public Dog(String col, float w) {
        color = col;
        weight = w;
    }

    public String getcolor() {
        return color;
    }

    public float getweight() {
        return weight;
    }

    public void setweight(int w) {
        weight = w;
    }

}
